package model;

/**
 *
 * @author Yure
 */
public class Cuidador {
    private int id_cuidador; 
    private String nome_cuidador;
    private String telefone_cuidador;
    private String endereco_cuidador; 
    private String email_cuidador;
    private String senha_cuidador; 

    public int getId_cuidador() {
        return id_cuidador;
    }

    public void setId_cuidador(int id_cuidador) {
        this.id_cuidador = id_cuidador;
    }

    public String getNome_cuidador() {
        return nome_cuidador;
    }

    public void setNome_cuidador(String nome_cuidador) {
        this.nome_cuidador = nome_cuidador;
    }

    public String getTelefone_cuidador() {
        return telefone_cuidador;
    }

    public void setTelefone_cuidador(String telefone_cuidador) {
        this.telefone_cuidador = telefone_cuidador;
    }

    public String getEndereco_cuidador() {
        return endereco_cuidador;
    }

    public void setEndereco_cuidador(String endereco_cuidador) {
        this.endereco_cuidador = endereco_cuidador;
    }

    public String getEmail_cuidador() {
        return email_cuidador;
    }

    public void setEmail_cuidador(String email_cuidador) {
        this.email_cuidador = email_cuidador;
    }

    public String getSenha_cuidador() {
        return senha_cuidador;
    }

    public void setSenha_cuidador(String senha_cuidador) {
        this.senha_cuidador = senha_cuidador;
    }
}
